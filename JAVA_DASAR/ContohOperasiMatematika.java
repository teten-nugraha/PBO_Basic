package JAVA_DASAR;

public class ContohOperasiMatematika {
  public static void main(String[]args) {
    byte a = 5;
    byte b = 2;

    int tambah = a + b;
    int kurang = a - b;
    float bagi = a / b;
    int kali = a * b;
    int mod = a % b;
    
    System.out.println("Hasil Tambah = "+ tambah);
    System.out.println("Hasil Kurang = "+ kurang);
    System.out.println("Hasil Bagi = "+ bagi);
    System.out.println("Hasil Kali = "+ kali);
    System.out.println("Hasil Sisa Hasil Bagi = "+ mod);
  }
}
