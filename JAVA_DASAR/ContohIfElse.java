public class ContohIfElse {
  public static void main(String[]args) {
    
    byte nilai = 78;

    if(nilai > 100) {
      System.out.println("Grade : A");
    }else if(nilai > 70) {
      System.out.println("Grade : B");
    }else{
      System.out.println("Grade : C");
    }

  }
}
