package JAVA_DASAR;

public class TipeDataNumerik {
  public static void main(String[]args) {
    byte data1 = 127;
    System.out.println("Nilai byte nya adalah "+data1);

    short data2 = 32767;
    System.out.println("Nilai short nya adalah "+data2);

    int data3 = 2147483647;
    System.out.println("Nilai int nya adalah "+data3);

    long data4 = 9223372036854775807L;
    System.out.println("Nilai long nya adalah "+data4);
  }
}
